package org.hw17;

class Pair{
    private String lowerCase;
    private String upperCase;

    public Pair(String lowerCase, String upperCase) {
        this.lowerCase = lowerCase;
        this.upperCase = upperCase;
    }

    public String getLowerCase() {
        return lowerCase;
    }

    public String getUpperCase() {
        return upperCase;
    }
}
